//Read sensor DHT11 function
void dht11(uint16_t *temp, uint16_t *humid)
{
	uint16_t tempcalc, humidcalc;
	// First step for communication is to configure PB9 (named 'dht11') as output
	/*Configure GPIO pin : dht11_Pin */
	GPIO_InitTypeDef GPIO_InitStruct = {0};
	GPIO_InitStruct.Pin = dht11_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
	HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

	// Set output level to '0' - datasheet
	HAL_GPIO_WritePin(GPIOB, dht11_Pin, GPIO_PIN_RESET);

	// Wait >18ms - datasheet
	HAL_Delay(20);

	// Set output level to '1' - datasheet
	HAL_GPIO_WritePin(GPIOB, dht11_Pin, GPIO_PIN_SET);

	// Next step for communication is to configure PB9 (named 'dht11') as input
	/*Configure GPIO pin : dht11_Pin */
	GPIO_InitStruct.Pin = dht11_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
	HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

	// Reading
	/*Set timer 2 to 0*/
	__HAL_TIM_SET_COUNTER(&htim2, 0);

	/*Aux variables*/
	uint16_t read[2];
	uint16_t data[42];
	uint8_t bits[40];
	uint16_t temph = 0;
	uint16_t humidh = 0;

	/*Detect time during High*/
	for(int i = 0; i < 42; i++)
	{
		while(HAL_GPIO_ReadPin(GPIOB, dht11_Pin) == GPIO_PIN_RESET);
		read[0] = __HAL_TIM_GET_COUNTER(&htim2);
		while(HAL_GPIO_ReadPin(GPIOB, dht11_Pin) == GPIO_PIN_SET);
		read[1] = __HAL_TIM_GET_COUNTER(&htim2);
		data[i] = read[1] - read[0];
	}

	/*Set bit reading according to times from datasheet*/
	for(int i = 0; i < 40; i++)
	{
		if((data[i+2] >= 20) && (data[i+2] <= 32))
		{
			bits[i] = 0;
		}
		else if((data[i+2] >= 65) && (data[i+2] <= 75))
		{
			bits[i] = 1;
		}
	}

	printf(bits);

	/*Calculate temp and humd according to the read bits*/
	for (int i = 0; i < 8; i++)
	{
		temph += bits[i+16] << (7-i);
		humidh += bits[i] << (7-i);
	}

	/*Set values to respective variables*/
	tempcalc = temph;
	humidcalc = humidh;
	*temp = tempcalc;
	*humid = humidcalc;

	/**/


}

